#include "Arduino.h"
#include "SoftPin.h"

int _pin;
int _last_V;
int _current_V;
int _target_V;
long _millis_at_change;

int _smoothTime;

extern const uint16_t gamma_1023[];

SoftPin::SoftPin(int pin, int smoothTime)
{
  pinMode(pin, OUTPUT);
  _pin = pin;
  _smoothTime = smoothTime;
}

/**
 *  Specify a target value between 0-1023
 */
void SoftPin::set(int value)
{
  if(value == _target_V){
    return;
  }
  value > 1023 ? value = 1023 : value;
  value < 0 ? value = 0 : value;
  _last_V = _current_V;
  _target_V = value;
  _millis_at_change = millis();
}

/**
 *  Specify a target value between 0-255
 */
void SoftPin::set255(int value255)
{
  value255 > 255 ? value255 = 255 : value255;
  value255 < 0 ? value255 = 0 : value255;
  float value = (float)value255 * 4.012;
  set(value);
}

/**
 *  Specify a target value between 0-100
 */
void SoftPin::set100(int value100)
{
  value100 > 100 ? value100 = 100 : value100;
  value100 < 0 ? value100 = 0 : value100;
  float value = (float)value100 * 10.23;
  set(value);
}

void SoftPin::loop()
{
  long distance = millis() - _millis_at_change;

  if(distance <= _smoothTime){
    float percentage = (float) distance / _smoothTime;
    _current_V = (float)percentage * (float)(_target_V - _last_V) + _last_V;
  }
  else{
    _current_V = _target_V;
  }

  analogWrite(_pin, pgm_read_word(&gamma_1023[_current_V]));
}

int SoftPin::getCurrentValue()
{
  return _current_V;
}

int SoftPin::getTargetValue()
{
  return _target_V;
}

int SoftPin::getLastValue()
{
  return _last_V;
}


